export default function App() {
  const [movies, setMovies] = useState([]);
  const [watched, setWatched] = useState([]);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState("");
  const [query, setQuery] = useState("inception"); 
  const [selectedId, setSelectedId] = useState("");
const[search, setSearch] = useState('') ;
  

  useEffect(() => {
    async function fetchMovies() {
      try {
        setIsLoading(true);
        setError("");

        const res = await fetch(
          `https://www.omdbapi.com/?apikey=605218fa&s=${query}`
        );

        if (!res.ok) throw new Error("Something went Wrong");

        const data = await res.json();
        if (data.Response === "False") throw new Error("Movie not found");

        setMovies(data.Search);
      } catch (err) {
        setError(err.message);
      } finally {
        setIsLoading(false);
      }
    }

    if (query.length < 3) {
      setMovies([]);
      setError("");
      return;
    }
    fetchMovies();
  }, [query]);

  function handleSelectedMovie(id) {
    setSelectedId((selectedId) => (id === selectedId ? null : id));
  }

  function handleCloseMovie() {
    setSelectedId(null);
  }
  function handleAddWatched(movie) {
    setWatched((watched) => [...watched, movie]);
  }

  function handleDeleteMovie(id){
    setWatched((watched)=> watched.filter(movie => movie.imdbID !== id) )
    console.log('lulu')
  }
  return (
    <>
      <Navbar>
        <Logo />
        <Search query={query} setQuery={setQuery} search={search} setSearch={setSearch} />
        {movies && <NumResults movies={movies} />}
      </Navbar>

      <Main>
        <Box>
          {isLoading && <p>Loading...</p>}
          {!isLoading && !error && (
            <MovieList movies={movies} onSelectMovie={handleSelectedMovie} />
          )}
          {error && <ErrorMessage message={error} />}
        </Box>
        <Box>
          {selectedId ? (
            <MovieDetails
              selectedId={selectedId}
              onCloseMovie={handleCloseMovie}
              onAddWatched={handleAddWatched}
            />
          ) : (
            <>
              <WatchedSummary watched={watched} />
              <WatchedMoviesList watched={watched} onDeleteMovie={handleDeleteMovie}/>
            </>
          )}
        </Box>
      </Main>
    </>
  );
}
